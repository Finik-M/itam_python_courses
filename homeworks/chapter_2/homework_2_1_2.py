a = input().split()
a1, b = [], []
def summation(a):
    for i in a:
        i = int(i)
        if i < 0:
            a1.append(i * -2)
        else:
            a1.append(i)
    mx = max(a1)
    for i in a1:
        b.append(i/mx)
    sm = sum(b)
    print(sm)
summation(a)